<h1>Code Konventionen</h1>

<p>
Codekonventionen werden häufig Missverstanden, vor allem von solchen Personen
die ernsthaft glauben ihre Vorstellung wie Code aussehen muss wäre richtiger
als irgend ein andere Format.
</p>

<p>
Vorrausgesetzt, dass die Formatierung logischen Regeln folgt ist es relativ
egal wie formatiert wird. Wichtig ist, dass innerhalb eines Projektes
einheitlich nach dem gleichen Schema gearbeitet wird.
</p>

