<h1>Patterns</h1>

<p>
Patterns bieten hilfreiche Vorlagen / und Lösungsansätze für häufig auftretende
Probleme in der Informatik.
</p>

<p>
Annerkannte Pattern sind meistens sehr durchdacht und von einer größeren Community
umfangreich Analysiert und Disskutiert.<br />
In der Hoffnung nicht allzufalsch zu liegen, wenn ich den Beteiligten eine
hohe Kompetenz auf diesem Gebiet unterstelle, können wir also davon ausgehen, 
dass diese Lösungen meist einen relativ optimalen Lösungsansatz bieten.
</p>

<p>
Wenn also das passende Pattern identifiziert wurde, es jedoch suboptimal für
den aktuellen Use Case erscheint, ist die Wahrscheinlichkeit höher, dass 
der Use-Case noch nicht komplett Verstanden wurde, als dass das Pattern tatsächlich
Suboptimal ist.
</p>

<p>
Nichtsdesto trotz sind Pattern nur relativ allgemein Vorlagen die nicht immer alle Aspekte
von jedem erdenklichen Spezialfall abdecken können.
</p>

<p>
Bevor ein Pattern eingesetzt wird ist es daher wichtig genau zu verstehen was
dieses Pattern macht, welche Auswirkungen es auf den Code / Programmfluss haben wird,
und welche Nachteile sich daraus theoretisch und/oder zwingend praktisch ergeben.<br />
Für eine korrekte Einschätzung eines Patterns in Relation zu einem Use-Case ist es natürlich
auch nötig, dass der Use-Case, sowie sich daraus ergebende Anforderungen an das System
genau verstanden wurden.
</p>

<p>
Zusammenfassung: in den meisten Fällen ist es sinnvoll Pattern einzusetzen.
Bevor bei der Implementierung von Pattern abgewichen werden soll, ist zu prüfen
ob das Tatsächlich eine gute Idee ist, oder ob nicht doch das Pattern den sinnvolleren Weg
vorgibt.<br />
Wird davon abgewichen, dann ist die Motivation mit den Argumenten warum es
getan wurde an einer Stelle zu dokumentieren, an der jemand der sich über
die Abweichung wundern könnte direkt beim wundern drüberfällt.<br />
Wenn Abweichungen sinnvoll verargumentiert werden können und mehr Vorteile als
Nachteile bringen, dann werden diese ohne weitere Diskussion umgesetzt.
</p>
