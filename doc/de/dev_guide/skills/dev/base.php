<h1>Developer Skills</h1>

<p>
Dieser Abschnitt Behandelt den Unterschied zwischen einem Programmierer 
und einem Professionellen Entwickler.
Die Definition für Programmierer ist in diesem kontext eine Person die mindestens
Programmiersprache beherrscht, also programmieren kann und das auch tut.
Ein Entwickler muss all das auch können, allerdings noch eine ganze Menge mehr.
</p>

<p>
Für die Professionelle Softwareentwicklung vor allem in Teams genügen reine 
Programmierkenntnisse, und eine Reihe von relativ allgemeinen Softskills schon
lange nichtmehr.
</p>

<p>
In der heutigen Zeit sind je nach Einsatzgebiet, gerade bei der Entwicklung
einer Softwareplatform die eine Breite Anforderungspalette abdecken muss, 
sind noch eine Reihe weiterer spezifischer Kentnisse nötig.
</p>


