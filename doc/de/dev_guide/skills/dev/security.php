<h1>Security</h1>

<p>
Willkommen an der Front des 21ten Jahrhunderts.
</p>

<p>
Informationen sind das Gold / Öl des 21ten Jahrhunderts. Nicht von ungefähr sind
Konzerne wie Google oder Facebook in kürzester Zeit an die Weltspitze aufgestiegen.
</p>

<p>
Daten wecken Begehrlichkeiten, das gilt vor allem bei Business Systemen wie
wir sie bauen, welche tendenziell sehr interessante und wichtige Daten enthalten. 
Geraten diese Daten in falsche Hände, werden sie manipuliert oder gehen verloren
dann bedeutet das in der Regel einen sehr großen,  oft auch existenziell bedrohlichen
Schaden für den Betreiber des Systems.
</p>

<p>
Mittlerweile dürften das praktisch alle Relevanten Entscheider in unsere Zielgruppe
mehr als verinnerlich haben. Ein existenziell wichtiger Punkt für uns ist daher
ein hohes Vertrauen potentieller Kunden in die Sicherheit des Systems.
</p>

<p>
Dieses Vertrauen kann sehr leicht verspielt, und dann auch nur noch 
sehr schwer zurückgewonnen werden. Die Erfahrung zeigt dabei dass, in der Regel
auch immer das Produkt / Team / die Firma für die Fehler weniger in Sippenhaft genommen werden.
</p>

<p>
Daher ist es wichtig, das jeder einzelne nach bestem Wissen und Gewissen nach den
Security Guidelines arbeitet, und das Thema Sicherheit nicht auf die leichte
Schulter nimmt.
</p>

<p>
<i>
In einem schulterhohen Schützengraben ist das laufen mit eigezogenem Kopf 
sicher nicht bequem, aber jedem dürfte klar sein, dass es eine verdammt blöde
Idee wäre sich aus Bequemlichkeit aufzurichten.
</i>
</p>