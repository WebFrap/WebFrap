<h1>Verhalte dich mündig</h1>

<p>
Wenn du die Fehler immer nur bei anderen suchts, dann ist das zwar bequem, aber bringt
dich keinen Schritt vorwärts.
</p>

<p>
Es gibt 2 Möglichkeiten: du selbst machst die Fehler, siehs das Problem aber bei anderen.
Wenn du auf dieser Position beharrst hast du keine Chance dich zu verbessern.
Du stehst dir selbst im Weg und hast kaum eine Chance, dass sich das ändert.
</p>

<p>
Möglichkeit 2, die Probleme kommen Tatsächlich von dem / den Anderen. Du kannst
nun guten Gewissens die Schuld auf den anderen schieben, aber du sitzt immernoch
in der Sackgasse weil sich immer noch keiner dran gemacht hat das Problem zu lösen.
</p>



