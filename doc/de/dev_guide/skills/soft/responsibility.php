<h1>Übernimm Verantwortung</h1>

<p>
Steh zu dem was du machst, im Guten so wie im Schlechten.
Wenn du eine Entscheidung treffen musst dann triff sie. Selbst wenn sie
Falsch  war bist trotz allem einen Schritt weiter, als wenn du sie noch nicht
getroffen hast.
</p>

<p>
Das ist keine Einladung für Schnellschüsse, trotz allem muss die Entscheidungsfindung
auf Fakten basieren.
</p>
