<h1>Disziplin</h1>

<p>
Hm jetzt kommt der Spielverderber mit Disziplin...
</p> 

<p>
Klingt irgendwie so ätzend einengend.
Wir sind doch alle kreative Menschen, ist Disziplin da nicht Gift?
</p>

<p>
  Der Codeumfang der kompletten WebFrap Plattfrom ist aktuell im 9 stelligen Bereich.
  Das bedeutet irgendwo zwischen <strong>100.000.000</strong> und <strong>999.999.999</strong> Zeilen Code.
  Die Milliarde ist bereits in Sicht. 
  Das sind Hunderttausende von Klassen und durch die Doku nochmal ein vielfaches davon an Dateien.
  Das sind Dimensionen in denen bei kleinen (V)-Servern die Inodes auf der Platte schom beim (Komplettsystem) deployen ausgehen können.
  Wenn ich jetzt sage, das ist das größte was du je in deinem Leben gesehen hast, werde ich in den
  allermeisten Fällen Recht behalten.
</p>

<p> 
  Zugegeben, die blose Anzahl Codezeilen ist nicht wirklich Aussagekräftig, und bei
  dieser Menge Code liegt die Vermutung sehr Nahe, dass dort mit viel heißer 
  Luft eine Wasserbombe zum Zeppelin aufgeblasen wurde.
  Das ist tatsächlich ein Problem, das es gerade erfahrenen Entwicklern und Entscheidern
  schwer fallen dürfte, nicht genau diese Bild ( oder ein Vergleichbares Bild ) im Kopf zu haben. 
</p>

<p>
  Aktuell sind grob geschätzt 95% des Codes durch die Architektur belegbar, 
  und in Relation zum Funktionsumfang auch angemessen. 
  Die restlichen bis zu 5% können als Puffer betrachtet werden, welcher dem Umstand geschuldet ist, 
  dass Architektur Refactoring ein Hauptbestandteil des Standard Entwicklungsprozesses darstellt.
  Für das überleben des Projektes ist es auch wichtig, dass diese Größenordnung bestehen bleibt,
  da die Plattform sonst (zurecht) durch solche Vorwürfe angreifbar wird.
</p>

<p>
  Genau deshalb ist es wichtig, dass alle sich diszipliniert in einem vorgegebene Rahmen
  bewegen, und Abweichungen davon mit den zuständigen Personen koordinieren.
</p>
