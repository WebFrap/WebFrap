<h1>Probleme lösen</h1>

<p>
Es gibt Menschen bei denen ist alles ein Problem. Solltest du dich in dieser
Beschreibung wieder erkennen such dir Psychologische Hilfe, du brauchst sie.
</p>
<p>
Da der soeben genannte Typ Mensch diese Zeilen vermutlich schon nichtmehr weiter 
liest können wir uns jetzt dem produktiven Teil zuwenden.
</p>

<p>
Einige großen Probleme sind oft nur so groß weil sie aufgeblasen wurden, anstelle sie zu lösen.
Bei Diskussion kann oft beobachtet werden, dass die Teilnehmer viel mehr damit beschäftigt
warum die Vorschläge der andere eben nicht funktionieren, anstelle gemeinsam
einen Lösungsansatz zu einer Lösung auszubauen.
</p>

<p>
Die erste Regel zum Probleme lösen ist miteinadern und nicht gegeneinander zu arbeiten.
Zu wahrer Größe gehört nunmal auch die Fähigkeit zu erkennen, wenn jemand anderes
einen besseren Lösungsansatz gefunden hat.
</p>
