<h1>Fehler machen</h1>

<p>
Wenn wir eins nicht können, das ist das augenscheinlich Fehler machen
und diese dann auch einzugestehen. Nun in einem Land (eher in einer Welt) 
in dem Politiker regelmäßig mit geradezu abstoßend schlechtem Beispiel 
vorangehen ist das auch nicht weiter verwundernlich.<br />
Wir sind aber keine Politiker!<br />
Wir produzieren reale / virtuelle Güter mit messbaren Qualitätsanforderungen.
Daher könne wir es uns nicht erlauben Verhaltensmuster zu dulden die primär in 
der Heißen-Luft-Produktion Anwendung finden.
</p>

<p>
Du wirst Fehler machen. Punkt. Ich weiß das, du weißt das und jeder andere
weiß das auch. Das ist ein Umstand der nicht zu ändern ist finde dich damit ab.
Das mussten alle andere ( inklusive mir ) auch lernen.
Das werden eine Menge kleiner unrelevanter Fehler sein, aber früher oder später
mit an Sicherheit grenzender Wahrscheinlichkeit irgendetwas so dämliches / peinliches oder auch
folgenträchtiges, dass du am liebsten durch den Boden versinken würdest. 
</p>

<p>
Das ist ok, du darfst Fehler machen. Fehler sind in den meisten Fällen kein Problem bis das kleinere Problem.
Zu einem Problem werden Fehler oft erst durch einen falschen Umgang mit ihnen.
</p>

<p>
Also wie gehen wir nun damit um? 
Fast alle Fehler, auser solche die zu einem sofortigen Weltuntergang führen, 
können wenn rechtzeitig bekannt behoben werden ohne das größerer Schaden entsteht.  
Wird die Lösung aus welchen Gründen auch immer verzögert potenziert sich der Schaden
im schlimmsten Fall.
Also sei kein Politiker und geh die Probleme an bevor sie wirklich problematisch werden.
</p> 

<p>
Egal wie dumm der Fehler war, dir wird keiner den Kopf abreisen wenn du angemessen darauf
reagierst. Es sei denn du hast grob Fahrlässig Anweisungen bewusst missachtet.
Vertuschungsversuche hingegen werden immer sehr schmerzhafte Konsequenzen
nach sich ziehen. Diese werden im Gegensatz zum Fehler vorsätzlich begangen, 
und sie bergen auch das weitaus größeres Gefahrenpotential in sich.
</p>

<p>
Halten wir fest, jeder hat das Privileg an seinen Fehlern ohne Vorwürfe wachsen
zu können, solange er aus ihnen lernt und angemessen auf sie reagiert.
</p>