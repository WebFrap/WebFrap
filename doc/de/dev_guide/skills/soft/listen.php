<h1>Zuhören</h1>

<p>
Gib den Menschen Gelegenheit das zu sagen was sie sagen wollen.
Versuche ihr Anliegen zu verstehen und vermeide schmerzhaft verzogene Gesichtsausdrücke, 
auch dann wenn du endlich verstanden hast was sie eigentlich sagen wollen.
</p>

<p>
Jeder Mensch hat das Grundbedüfniss ernst genommen zu werden und es lassen sich
viele Konflikte vermeiden oder in Vernünftige Bahnen lenken, wenn der Gegenüber
das Gefühl hat ernst genommen zu werden.
</p>

