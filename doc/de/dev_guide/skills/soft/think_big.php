<h1>Think Big... aber richtig!</h1>

<p>
Wenn ich hier schreibe "think big", dann verwechsel das bitte nicht mit den
sonst nicht unübliche Einladung zum Größenwahnsinn.
Wir kochen ( wie alle anderen ) auch nur mit Wasser...<br />
nur das Was und Wie hebt uns von den andere ab.
</p>

<p>
Und jetzt kommen wir auch schon zu einem Teil der (Wirtschafts-)Informatik der 
auf jeden Fall um den Rang der Königsdisziplin mitkandidieren kann.
</p>

<p>
Unsere Wirtschaft basiert auf einem ewigen Wachstum ( Zugegegeben das praktisch
alle Systeme irgendwann kolabieren beschränkt der Begriff der Ewigkeit ein bisschen ).
Für uns bedeutet dass, das die Systeme in der Regel sowohl von der Technik / dem Leistungsbedarf
als aus Anwendungsfällen wachsen können müssen.
</p>

<p>
Oft ist schon im Ansatz erkennbar wohin die Reise gehen wird, und deine Aufgabe
ist es zu lernen diese Zeichen zu deuten und richtig ein zu schätzen.
</p>

