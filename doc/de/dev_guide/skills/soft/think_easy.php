<h1>Think easy</h1>

<cite>
Debugging is twice as hard as writing the code in the first place. 
Therefore, if you write the code as cleverly as possible, you are, by definition, not smart enough to debug it.
</cite>
<p>- Brian W. Kernighan</p>

<p>
Die Komplexität kommt besonders bei unsere Use-Cases über den Umfang der Anfoderungen.
Die Gesamtkomplexität des Projektes ist das Produkt aus den Anfoderungen und dem
Weg diese umzusetzen.<br />
Die logische Konsequenz daraus ist, das je komplizierter der Ansatz desto höher
das Risiko an der Gesamtkomplexität zu scheitern.
</p>