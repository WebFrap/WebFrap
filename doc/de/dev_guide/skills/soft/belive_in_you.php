<h1>Glaub an dich</h1>

<p>
Gerade am Anfang herrschen bei viele Zweifel vor, ob die gewählte Lösung denn
nun auch so die Richtige ist, oder ob man das nicht viel besser, eleganter, schöner... whatever machen könnte.
</p>

<p>
Die Zweifel sind unbegründet. <br />
Es ist vollkommen klar, dass es eine bessere, schönere und elegantere Lösung gibt 
gerade wenn Erfahrung noch Mangelware ist. (Wozu bräuchte man sie auch sonst?) <br />
Diese besagte Superlösung ist jedoch nicht unbedingt richtiger, nicht zwangweise
wesentlich performanter und muss auch keinen wirklich relevanten Vorteil bringen.
(Davon ausgehend, dass die aktuelle Lösung nicht tatsächlich Mängel aufweist
die zu realen / erkennbaren Problemen führt.)</br >
Aber es gibt sie wie du bereits vermutet hast sehr wahrscheinlich.
Höchstwahrscheinlich gibt es sogar mehrere Varianten dieser Superlösung, so dass nicht 
abschliesen geklärt werden kann welche denn nun die Beste, Tollste... $WhateverKriterum ist.
</p>

<p>
Dummerweise wirst du mindestens das 10fache an Zeit benötigen wenn du ständig auf der 
Suche nach dieser Lösung bist, um dir dann damit einen marginalen Vorteil 
gegenüber der Lösung zu erkaufen die du gerade gebaut hast.<br />
Und wenn du dann doch einmal die Überlösung gefunden hast und diese als Nonplusultra
propagieren möchtest kommen X Vertreter andere Ansätze welche deine Lösung mit irgendwelchen
für dich eigentlich gar nicht relevanten Einwänden / Anwendunsfällen zerlegen werden.
</p>

<p>
Halten wir fest, was auch immer du mit dem optimieren einer bereits funktionierenden Lösung 
gewinnen willst, solange es nicht nötig ist verlierst du nur Zeit welche du besser 
an einer anderen Stelle investiert hättest.
</p>

<p>
Also wenn du das nächste mal wieder vor einer Aufgabe verzweifelst, fang einfach mit
dem Teil an den du verstanden hast. Der Rest ergibt sich meist beim Arbeiten von aleine.
</p>
