<h1>Übersicht über die WebFrap Repositories</h1>

<p>Es gibt innerhalb von WebFrap 5 Repository Container</p>

<label>Container</label>
<ul>
  <li>Core https://hg.webfrap-servers.de/core</li>
  <li>GenF https://hg.webfrap-servers.de/genf</li>
  <li>Buiz https://hg.webfrap-servers.de/buiz</li>
  <li>Demo https://hg.webfrap-servers.de/demo</li>
  <li>Vendor https://hg.webfrap-servers.de/vendor</li>
</ul>

<p>Alle Repositories enthalten mindestens 3 Branches</p>

<ul>
  <li>stable</li>
  <li>testing</li>
  <li>development</li>
</ul>


