<h1>Aufbewahrungsfristen</h1>

<p>Folgende Informationen beziehen sich nur auf die Bundesrepublik Deutschland.</p>

<h2>Personenbezogene Daten</h2>

<p>Personenbezogene Daten in Dateien sind zu löschen:</p>

<ul>
  <li>Wenn sich heraus stellt, dass ihre Speicherung unzulässig war / ist</li>
  <li>Sobald oder ihre Kenntnis für die speichernde Stelle zur Erfüllung ihrer Aufgaben nicht mehr erforderlich ist.</li>
  <li>Komplett Datensätze zu Personen sind zu löschen oder zu anonymisieren, sobald diese zur Aufgabenerfüllung
      des Systems nicht mehr erforderlich ist.
  </li>
</ul>

<h2>Unternehmerische Dokumente nach HGB §238 und HGB §257</h2>

<p>
Die Aufbewahrunsfrist für den Datensatz / da Dokument beginnt zum Ende des Kalenderjahres
in dem der Datensatz erstellt wurde.
</p>


<h3>Eine Frist von 6 Jahren gilt für:</h3>

<ul class="doc_tree" >
  <li>Handelsbezogene Kommunikation, Versand und Empfang</li>
  <li>Alle Kaufmänisch relevanten Dokumente oder Datensätze</li>
  <li>Sonstige Informationen zu Geschäftsvorfällen die theoretisch in Rechtsstreitigkeiten enden könnten</li>
</ul>

<h3>Eine Frist von 10 Jahren gilt für:</h3>

<p>Alle buchungsrelevanten Informationen / alle Buchungen</p>

<ul class="doc_tree" >
  <li>Jahresabschlüsse</li>
  <li>Inventare</li>
  <li>Handelsbücher</li>
  <li>Rechnungen</li>
  <li>Hypotheken</li>
  <li>etc.</li>
</ul>

<h3>Dauerhaft zu speichern sind:</h3>

<ul class="doc_tree" >
  <li>Baupläne</li>
  <li>Gerichtsurteile</li>
</ul>