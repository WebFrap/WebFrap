<h1>Personenbezogene Daten</h1>

<div>
<cite>
Personenbezogene Daten sind Einzelangaben über persönliche oder sachliche Verhältnisse einer bestimmten oder 
bestimmbaren natürlichen Person (Betroffener).
</cite>
</div>

<p>
<a class="cite-source" href="http://www.landesrecht-bw.de/jportal/?quelle=jlink&query=DSG+BW+%C2%A7+3&psml=bsbawueprod.psml&max=true" >-Gesetz zum Schutz personenbezogener Daten
(Landesdatenschutzgesetz - LDSG)
in der Fassung vom 18. September 2000</a>
</p>

<p>
Hinweis: die hier aufgeführten Daten erheben keinen Anspruch auf Vollständigkeit sondern
sollen nur als konkrete Beispiele zum verdeutlichen der Begrifflichkeiten verstanden werden.
</p>

<label>Beispiele für personenbezogene Daten</label>
<ul class="doc_tree" >
  <li>Vorname, Nachname, Titel</li>
  <li>Adresse</li>
  <li>Kontakt IDs wie Telefonnummer, E-Mail, Fax, ICQ ect.</li>
  <li>IP-Adresse</li>
  <li>GEO Koordinaten (Mobile Dienste)</li>
  <li>Sonstige Angaben, wie Geburtsdatum, Geb.Land, Haarfarbe, Geschlecht</li>
</ul>

<p>Darüber hinaus gibt es Daten welche als besonders kritisch und damit auch besonders schützenswert angesehen werden:</p>

<label>Beispiele für besondere Personenbezogene Daten</label>
<ul class="doc_tree" >
  <li>ethische Herkunft</li>
  <li>Relgion und Weltbild</li>
  <li>Zugehörigkeit zu Gewerkschaften oder politischen Gruppierungen</li>
  <li>Informationen zur Sexualität</li>
  <li>Angaben zur Gesundheit / körperlichem Zustand / Krankengeschichte</li>
  <li>Sonstige Angaben, wie Geburtsdatum, Geb.Land, Haarfarbe, Geschlecht</li>
</ul>

<p>
Ausführlichere Informationen zur Definition finden sich auf 
  <a href="http://de.wikipedia.org/wiki/Datenschutz" >http://de.wikipedia.org/wiki/Datenschutz</a>
</p>