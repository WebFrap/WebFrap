<h1>Dokumentation über die Speicherorte der Daten</h1>

<p>
Um einen sicheren Umgang mit den Daten und vor allem eine vollständiges löschen der
Daten zum Fälligkeitsdatum der Daten zu gewährleisten, ist zu dokumentieren wo sich
welche Daten, warum, in welcher Form und für wie lange befinden.
Gemeint is keine Protokollierung sondern eine Architektur und Ablaufbeschreibung. 
</p>

<label>Beispiele zur Verdeutlichung</label>
<ul class="doc_tree" >
  <li>Daten befinden sich in Datenbank in folgenden Tabellen und Attributen</li>
  <li>Lokale Caches auf dem Server</li>
  <li>Cachingserver</li>
  <li>Transfer der Daten</li>
  <li>Imports, woher werden welche Informationen mit welcher Priorität warum bezogen</li>
  <li>Exports, wer kann welche Informationen warum in welcher Form exportieren</li>
  <li>Speichern der Informationen auf Clients (IndexDB,WebDb, Browser Cache, sonstige Clients)</li>
  <li>Speichern der Informationen lokal in verarbeiteter Form, generierte Rechnungen, Reports etc.</li>
  <li>Inhalt der Backups, der Speicherort sowie das Format</li>
</ul>

<p>
Ebenfalls zu dokumentieren ist, wie sicher gestellt wird, dass die Daten unzugänglich gemacht
oder bei Bedarf gelöscht werden.
</p>



