<h1>Datenschutz</h1>

<p>
Der Begriff Datenschutz umfasst den Schutz aller schützenswerter Daten
innerhalb Informationsverabeitender Systeme / Arbeitsabläufen.
Der Schutzbegriff umfasst das Auslesen, Manipulieren oder Vernichten dieser Daten.
</p>

<ul class="doc_tree" >
  <li><span>Erhebung</span> = Beschaffen, § 3 Abs. 3 BDSG.</li>
  <li><span>Verarbeitung</span> = Speichern, Verändern, Übermitteln, Sperren, Löschen, § 3 Abs. 4 BDSG.</li>
  <li><span>Nutzen</span> = Jedes Verwenden, soweit es sich nicht um Verarbeiten handelt, d.h. Verwenden ist der Oberbegriff für Verarbeiten und Nutzen, § 3 Abs. 5 BDSG.</li>
</ul>

<p>
Abgesehen davon, dass Datenschutz auch Investitionsschutz, Schutz vor schlechter
Publicity und Schutz vor rechtlichen Konsequenzen im Falle des Nichtschutzes mit sich bringt,
gebietet es einfach schon die Ethik. <br />
Jeder Mensch hat das Grundrecht (sollte es zumindest haben) auf informationelle 
Selbstbestimmung, und aleine die Erkenntnis selbst nur ein Mensch zu sein
gebietet es dieses Grundsatz ernst zu nehmen und sich für seine Umsetzung im
Rahmen seiner persönlichen Möglichkeiten dafür einzusetzen. 
</p>

<label>Anerknnte Prinzipien nach der „Erklärung von Montreux“:</label>

<ul class="doc_tree" >
  <li>Zulässigkeit und Rechtmäßigkeit der Erhebung und Verarbeitung der Daten</li>
  <li>Richtigkeit</li>
  <li>Zweckgebundenheit</li>
  <li>Verhältnismäßigkeit (vgl. Verhältnismäßigkeitsprinzip)</li>
  <li>Transparenz</li>
  <li>individuellen Mitsprache und namentlich der Garantie des Zugriffsrechts für die betroffenen Personen</li>
  <li>Nicht-Diskriminierung</li>
  <li>Sicherheit</li>
  <li>Haftung</li>
  <li>unabhängige Überwachung und gesetzlicher Sanktionen</li>
  <li>angemessenes Schutzniveaus bei grenzüberschreitendem Datenverkehr</li>
</ul>

<p>
Ausformuliert bedeutet es, dass nur solche Daten gesammelt werden sollen
die für den Anwendungszweck tatsächlich nötig sind, und die Daten nur so lange zu
speicher, wie es die Umstände erfordern.
</p>
