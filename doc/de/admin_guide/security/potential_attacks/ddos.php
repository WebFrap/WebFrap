<h1>(D)Dos, (Distributed) Denial of Service Angriffe</h1>

<p>
Ziel dieser Attacke ist es ein IT System oder eine komplette Struktur durch
den das Ausnutzen technischer Fehler, dem vielfachen Anfragen resourcen intensiver
Dienste oder einfach durch eine exorbitant hohe Anzahl von Anfragen davon abzuhalten
seine Dienst ordnunsgemäß zu verrichten.
</p>

<p>
Das Thema ist zu komplex um auf nähere Details einzugehen. Wichtig ist hier
das Bewusstsein, dass bei solche Szenarien nur noch das Vorhandensein kompetenter
Mitarbeiter oder Servicepartner einen gewissen Schutz bieten kann, welche in der
Lage sein sollten geeignete Gegenmaßnahmen umzusetzen. (Soweit die technischen 
Gegebenheiten das überhaupt zulassen.)
</p>

<p>Bei Intranet Anwendungen können derartige Angriffsszenarios in der Regel vernachlässigt werden.</p>