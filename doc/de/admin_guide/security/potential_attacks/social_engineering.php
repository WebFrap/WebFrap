<h1>Social Engineering</h1>

<p>
Social Engineering umfasst Techniken, die auf der ersten Blick nichts mit
dem von Hollywood geprägten Bild des Hackers zu tun haben.
</p>

<p>
Ziel ist es durch soziale Interaktion über möglichst viele Informationen
über das Zielsystem, oder sogar schon an die gewünschten Zieldate zu kommen.
Oft gehen derartige Angriffe späteren technischen Angriffen vorraus, daher ist
es gerade hier wichtig zeitnah Angriffe als solche zu erkennen, um geeignete
Gegenmaßnamen für das sich aus daraus entstehenden Bedrohungspotential zu ergreifen.
</p>

<label>Beispiele für Social Engineering</label>
<ul class="doc_tree" >
  <li>Einfach Kommunikation, ausfragen des Opfers mit Hielfe psychologischer Tricks
    <ul>
      <li>Selbstdarstellungstrieb</li>
      <li>Konsequenzfallen</li>
      <li>Alkohol oder Drogeneinfluss</li>
      <li>Ausnutzen körperlicher Reize</li>
    </ul>
  </li>
  <li>Vorspiegeln falscher Tatsachen / Betrug</li>
  <li>Bestechung</li>
  <li>Erpressung</li>
  <li>Diebstahl</li>
  <li>Fotos des Arbeitsplatzes (Zettel mit Serverpwd am Monitor)</li>
  <li>Durchwühlen des Mülls nach verwertbaren Informationen</li>
</ul>

<p>
Gerade bei "Social engineering" ist es wichtig die Mitarbeiter zu Schulen und
ihnen ein Bewusstsein, aber auch den Rückhalt und Selbstsicherheit zu geben um solche Angriffe erkennen
und ihnen widerstehen zu können. Dazu ist es auch wichtig, dass Kompetenzen
und Zuständigkeiten genau definiert und dokumentiert sind, so dass Mitarbeiter
im Zeifelsfall Anfragen aus dem Prozess heraus dem Zuständigen und Sachkompetenten Mitarbeiter
weiterreichen, und nicht eigenmächtig potentiell gefährliche Fehlentscheidungen treffen.
</p>

<p>
Sehr wichtig dabei ist es, auch dem Mitarbeiter unter allen Umständen den 
Rücken zu stärken, so dass er ohne schlechtes Gewissen "NEIN" zu wem auch immer sagen kann, 
wenn er das als angemessen empfindet.
</p>

<label>Beispiele für potentiell gefährliche Anfragen:</label>
<ul class="doc_tree" >
  <li>Leite mir doch bitte mal Dokument XY weiter</li>
  <li>Leg mir doch bitte mal eine Account auf System XY an</li>
  <li>Trag mich bitte als Manager für... ein</li>
</ul>

