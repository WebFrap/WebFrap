<label>Liste potentieller Angreifer</label>

<ul class="doc_tree" >
  <li>
    <span class="value" >Mitarbeiter / Insider</span> Von Insidern, also solchen 
    Personen die Zugriffsberechtigung auf Daten erhalten haben geht das Größte Risiko aus.
    Praktisch in jeder Statistik machen Angriffe aus dieser Gruppe anteilmäßig signifikant mehr
    als die Hälfte aus. Das ist deshalb tragisch, da diese Personen vergleichsweise Unauffällig, mit wenig Aufwand
    and in der Regel viele (zu viele) Daten kommen.  Ob das System nun als Intranet oder als Global erreichbar ausgelegt
    ist, macht keinen nennenswerten Unterschied.
  </li>
  <li>
    <span class="value" >Unqualifizierte Angreifer</span> auch als Scriptkiddies bezeichnet. 
    Bei gut gewarteten Systemen spielen diese Angreifer nur eine untergeordnete Rolle. Bei
    Internen Systemen können sie sogar vernachlässigt werden. Lediglich bei externen Systemen
    können sie durch für kurze Perioden ernsthaft gefährlich werden, und zwar dann wenn neue
    Angriffstools für bestehende jedoch noch nicht gestopfte Lücken in Umlauf geraten. 
    Auf Grund des real monetären Wertes solcher Lücken, und dem steigenden Marktwert dieser Lücken
    ist die Wahrscheinlichkeit für derartige Vorfälle relativ geringt.
    Eine Angemessene Update Strategie ist ausreichend um das Risiko hier auf das mögliche Minimum zu drücken.
  </li>
  <li>
    Als <span class="value" >Qualifizierte Angreifer</span> sind solche Personen
    zu betrachten, welche sehr genau wissen was sie tun. Sie gehen in der Regel 
    gezielt und planmäßig vor um ein definiertes Ziel zu erreichen.
  </li>
</ul>

