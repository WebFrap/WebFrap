<h1>Potentielle Angreifer</h1>

<p>
Ich bin doch lieb und nett, zu klein, zu unbedeutend, versteckt... whatever.
Mich wird doch keiner Angreifen wollen, oder?
</p>

<p>
Nun die Zahlen hier sprechen ganz eindeutig eine andere Sprache.
Jede Art von schützenswerter Information, also solche Informationen welche nicht
frei zugänglich gemacht werden, wecken Begehrlichkeiten. Und Begehrlichkeiten
verlangen nach Befriedigung, die Realität ist gegen uns.
</p>

<p>
Die Art des Systems hat dabei nur einen vergleichsweise geringen Einfluss auf 
die potentiellen Angriffszenarien / Typen von Angreifern.
Lediglich die Wahrscheinlichkeiten für bestimmten Angreifertypen variieren. 
</p>

