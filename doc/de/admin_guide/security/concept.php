<h1>Sicherheitskonzept</h1>

<p>
Die <strong>Sicherheit des Gesamtsystems</strong> kann nur so stark (oder eben so 
schwach) wie das schwächste Glied in der Kette sein.
Für einen sicheren Betrieb der Plattform sind daher eine Reihe rudimentärer Verhaltensregeln nötig.
Die Einhaltung dieser Regeln kann zwar nicht verhindern, 
aber zumindest das Risiko vermindern, dass Schwachstellen ernsthafte Lücken in das System reisen. 
</p>

<p>
Bei diesem Spiel ist die Absichernde Seite per Definition im Nachteil.
Um einen Zustand zu erreichen der als Sicher bezeichnet werden kann
müssten praktisch alle Schwachstellen gefunden und beseitigt werden.
Für den Erfolg des Angreifer hingegen genügt ihm eine einzige aus-nutzbare Schwachstelle.
</p>

<p>
Nicht jeder Angriff hat das gleiche Gefährdungspotential. Weiter gibt es für fast 
alle Angriffsszenarien Präventivoptionen oder Reaktionsmöglichkeiten, welche das 
Schadenspotential auf das technisch mögliche Minimum reduzieren können. 
Details dazu werden im späteren Verlauf beschrieben. 
</p>

<p>
Die wichtigste Regel die es zu verstehen und  zu akzeptieren gilt ist, dass es keine absolute Sicherheit geben kann.<br />
Die einzige Chance für den Verteidiger ist es das System so weit abzusichern, dass der Aufwand für Angriffe die 
Wirtschaftlichkeit des Angriffs selbst in Frage stellt. <br />
Ist das Opfer relativ unattraktiv gibt der Angreifer möglicherweise auf und wendet sich leichteren Opfern zu. 
</p>
