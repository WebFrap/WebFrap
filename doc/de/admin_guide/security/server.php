<h1>Server</h1>

<p>
Die komplette Beschreibung wie ein Server abzusichern ist, würden den Rahmen dieses
Textes um ein Vielfaches sprengen. Daher geht dieser Abschnitt nur die Anforderungen
an den Server-Admin ein.
</p>

<p>
Oft werden Server nebenbei von den Entwicklern betreut, 
wobei die Betreuung sich häufig nur auf das initiale Aufsetzen des Servers beschränkt. 
Es ist unschwer zu erkennen, dass dies gegen alle essentiellen Grundbedingungen für den 
Betrieb sicherer IT Systeme verstößt.
</p>

<p>
Die wichtigste Regel für den Betrieb der Server ist, dass sie durch fachkundige
Kräfte regelmäßig betreut und gewartet werden müssen. Wichtig ist, dass die
Betretung ihre Hauptaufgabe ist, und nicht etwas das sie "nebenbei mal machen". <br />
Wenn die Dimension der IT keine Vollzeitelle rechtfertigt sind externe Ressourcen
dazu in Anspruch zu nehmen.
</p>

<p>
<strong>Unterbleibt eine kompetente Wartung</strong> der Server, aus welchen Gründen auch immer,
( sind wir ehrlich es sind praktisch immer die Kosten ) so wird diese
Ersparnis (von was auch immer) durch das <strong>Risiko eines Komplettverlustes der Daten</strong>
und in der Regel damit auch ein <strong>Verlust der Geschäftsgrundlage</strong>, oder einem nur schwer wieder
gut zu machenden <strong>Image Schaden</strong>.
</p>

