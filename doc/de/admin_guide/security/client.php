<h1>Clients</h1>

<p>
Wie bereits erwähnt ist das System nur so stark wie sein schwächstes Glied.
Hierbei ist es wichtig zu erkennen, dass auch der Client mit welchem auf die Daten zugegriffen
wird, ein wichtiger Teil in der Informationskette ist.
</p>

<p>
Bei Intranetszenarie gibt es für den Betreiber konkrete Möglichkeiten die Sicherheit der
Clients innerhalb eins vom Aufwand her vertretbaren Rahmens zu gewährleisten.<br />
Sobald jedoch beliebige Personen, über das Internet Zugriff bekommen entfällt diese Möglichkeit<br />
Gerade dann muss davon ausgegangen werden, dass ein nicht unerheblicher Teil der 
Clients mit Schadsoftware belastet ist.
</p>

<p>Hier helfen wenn überhaupt nur noch Risiko minimierende Maßnahmen, siehe Unterpunkte.</p>

