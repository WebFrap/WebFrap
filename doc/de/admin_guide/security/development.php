<h1>Entwicklung</h1>

<p>
Die Art der Entwicklung hab ebenfalls einen großen Einfluss auf die spätere Sicherheit des Systems.
Je größer der Zeitdruck und desto weniger Zeit zum Testen verplant wird desto größer
das Risiko unentdeckter Sicherheitslücken.
</p>

<p>
Ein weiteres Risiko besteht in der Nichteinhaltung der Sicherheitsvorgaben, z.B.
durch Unwille oder Unkenntnis. 
</p>
