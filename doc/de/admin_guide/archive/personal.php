<h1>Personalisierte Archive</h1>

<p>
Bdl bietet die Möglichkeit Baumartige Datenstrukturen im Modell zu definieren,
welche es ermöglichen Datensätze mitsamt Referenzen in beliebiger Tiefe als XML 
zu exportieren (bei bedarf auch zu reimportieren).
</p>

<p>
Durch diese Exports soll es ermöglich werden, dass z.B. 
Projektstrukturen, mit dazugehörigen Tasks, Buchungen, Abrechnungen als Datensatz
zusammen mit den Projektdateien gesondert archiviert werden können.
Sobald sich dies die Kontrolle dieser Archivierung dem zuständigen Datenschutzbeauftragten
entzieht ist darauf zu achten, dass diese Archive keine personenbezogene Daten enthalten
oder diese nur in einer Annonymisierten unkritischen form.
</p>
