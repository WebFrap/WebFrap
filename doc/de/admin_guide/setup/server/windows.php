<h1>Windows</h1>

<p>
Es gibt noch keinen offiziellen Support für Windows als Server.
Es sind zwar keine Probleme bekannt die dagegen sprechen einen Windowsbasierten
Server zu verwenden, es wurden jedoch auch noch keine ausführlichen Tests
durchgeführt um eventuell doch vorhandene Probleme auszuschliesen, da es bisher
auch noch keiene Anfrage für Windowsserver gab.
</p>

<p>
Wenn keine wirklich wichtigen Gründe dagegen sprechen empfehlen wir für den Betrieb
einen Linux oder BSD Server.
</p>

