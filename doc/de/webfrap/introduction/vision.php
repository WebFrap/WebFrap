<h1>Vision und Zielsetzung</h1>

<p>Die Frage die fast jeder stellt ist:
"Es gibt bereits eine Vielzahl von PHP Web Frameworks, warum also noch ein Neues?"
</p>

<p>
Nun WebFrap ist kein neues Framework, WebFrap wurde Anfang 2003 gestartet und wurde
mit dem Release von PHP 5.0 im Jahre 2004 zum einem der ersten komplett OOP basierten
PHP Frameworks.
Der Fokus war von Anfang an eine saubere erweiterbare Architektur ohne Altlasten.
</p>

<p>
Im Herbst 2005 kam mit GenF das Generator Framework dazu welches die weitere
Entwicklung von WebFrap hin zu einer Platform für effizient generierbare Applikationen.
</p>

<p>
Ende 2007 bekam WebFrap mit WGT ein eigene Web Gui Toolkit + CSS Framework 
basierend auf jQuery, später dann auf jQuery UI.
Anfang 2008 wurde durch das Einführen von Theme und Icon Projekten die Architektur
komplettiert die heute noch für WebFrap charakteristisch ist.
</p>

<p>
Das macht WebFrap zu einer kompletten Plattform die sämtliche Aspekte Webbasiertes
Applikationen durch sauber aufeinander abgestimmte Bibliotheken und Frameworks
beinhaltet.
</p>

<p>
WebFrap ist wie es der Name schon sagt nicht als Framework sondern als eine Frameapplication
konzipiert, also eine leere Application die durch Module mit Business Funktionalität erfüllt wird.
</p>

<p>
Um einen Praxisbezug sicher zu stellen wurden diverse Kundeprojekte basierend 
auf stabilen Prototypen von WebFrap erstellt und erfolgreich abgeschlossen.
</p>




